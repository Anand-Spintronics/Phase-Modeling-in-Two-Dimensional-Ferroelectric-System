P = -Pr * np.ones((Nx, Ny))
def Initial_Polarization():
  #P = -Pr * np.ones((Nx, Ny))
  # Center
  r_center = np.sqrt((X - Nx//2)**2 + (Y - Ny//2)**2)
  # Corners (quarter circles due to Neumann BC)
  r_corners = [
    np.sqrt((X - 0)**2     + (Y - 0)**2),
    np.sqrt((X - (Nx-1))**2 + (Y - 0)**2),
    np.sqrt((X - 0)**2     + (Y - (Ny-1))**2),
    np.sqrt((X - (Nx-1))**2 + (Y - (Ny-1))**2),
    ]
  P_center = Pr*np.tanh((R0-r_center)/w)
  mask_center = r_center < R0 + 3*w
  P[mask_center] = P_center[mask_center]
  for r in r_corners:
    P_corner = Pr * np.tanh((R0 - r) / w)
    mask_corner = r < R0 + 3*w
    P[mask_corner] = P_corner[mask_corner]
  return P
