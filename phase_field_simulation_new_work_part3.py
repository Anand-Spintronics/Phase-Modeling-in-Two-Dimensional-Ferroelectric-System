def laplacian_neumann(P, dx):
    lapP = np.zeros_like(P)

    # Interior points
    lapP[1:-1, 1:-1] = (
        P[2:, 1:-1] + P[:-2, 1:-1] +
        P[1:-1, 2:] + P[1:-1, :-2]
        - 4.0 * P[1:-1, 1:-1]
    ) / dx**2

    # Edges (zero normal gradient)
    lapP[0, 1:-1] = (
        P[1, 1:-1] + P[0, 1:-1] +
        P[0, 2:] + P[0, :-2]
        - 4.0 * P[0, 1:-1]
    ) / dx**2

    lapP[-1, 1:-1] = (
        P[-2, 1:-1] + P[-1, 1:-1] +
        P[-1, 2:] + P[-1, :-2]
        - 4.0 * P[-1, 1:-1]
    ) / dx**2

    lapP[1:-1, 0] = (
        P[2:, 0] + P[:-2, 0] +
        P[1:-1, 1] + P[1:-1, 0]
        - 4.0 * P[1:-1, 0]
    ) / dx**2

    lapP[1:-1, -1] = (
        P[2:, -1] + P[:-2, -1] +
        P[1:-1, -2] + P[1:-1, -1]
        - 4.0 * P[1:-1, -1]
    ) / dx**2

    # Corners
    lapP[0, 0] = (
        P[1, 0] + P[0, 0] +
        P[0, 1] + P[0, 0]
        - 4.0 * P[0, 0]
    ) / dx**2

    lapP[0, -1] = (
        P[1, -1] + P[0, -1] +
        P[0, -2] + P[0, -1]
        - 4.0 * P[0, -1]
    ) / dx**2

    lapP[-1, 0] = (
        P[-2, 0] + P[-1, 0] +
        P[-1, 1] + P[-1, 0]
        - 4.0 * P[-1, 0]
    ) / dx**2

    lapP[-1, -1] = (
        P[-2, -1] + P[-1, -1] +
        P[-1, -2] + P[-1, -1]
        - 4.0 * P[-1, -1]
    ) / dx**2

    return lapP

